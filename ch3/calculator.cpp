#include <iostream>
using namespace std;
int main(){
    double firstNum;
    double secondNum;
    cout << "Enter First number: ";
    cin >> firstNum;
    cout << "Enter second Number: ";
    cin >> secondNum;
    cout << "sum = "<< firstNum+secondNum << "\n";
    cout <<"difference = "<< firstNum-secondNum << "\n";
    cout << "product = "<< firstNum*secondNum << "\n";
    cout << "quotient = "<< firstNum/secondNum << "\n";
}